"""Sample Proposals Library -- a curated shelf of example proposals.

ORA Navigator coaches the *writing* of a proposal but never showed a PI what a
fundable proposal actually looks like. This module backs the /sample-proposals
page. It mixes two kinds of entries (a "hybrid" library):

  - type "pdf"  -- ORIGINAL sample proposals we authored ourselves and host as
                   downloadable PDFs (see scripts/make_sample_proposals.py).
                   Because we wrote them, there is zero copyright exposure and a
                   PI can download and read them offline. These are the most
                   useful entries for a first-timer, so they sort first.
  - type "link" -- vetted links OUT to authoritative public libraries (NSF's
                   official sample, NIH/NIAID funded apps + reviewer critiques,
                   university collections, Open Grants). We never rehost these
                   (they're copyrighted and many are index pages, not single
                   files) -- the card opens the source in a new tab.

Design (mirrors services/forms_catalog.py and main.DEFAULT_QUESTION_POOL): the
data is a static Python constant -- no DB, no embeddings, no network. The hosted
PDFs live in backend/sample_proposals/ (shipped in the Docker image) and are
served by GET /api/sample-proposals/{id}/download.

Each entry is tagged with one or more `categories` (the UI filter chips).
list_samples(category) backs the listing endpoint; get_sample(id) + pdf_path(id)
back the download endpoint. Nothing here raises.

When adding an entry: give it a unique `id` and at least one tag from CATEGORIES.
A "pdf" entry needs a `pdf` filename present in backend/sample_proposals/; a
"link" entry needs an https `url`.
"""
